@bot.message_handler(commands=['start'])
def start(message):
    bot.send_message(
        message.chat.id,
        "👋 Здравствуйте!\n\nЯ официальный информационный бот депутата **Владимира Викторовича Смирнова**.\n\nВыберите нужный раздел:",
        parse_mode='Markdown',
        reply_markup=main_menu()
    )

@bot.callback_query_handler(func=lambda call: True)
def callback_handler(call):
    text = ""
    if call.data == "bio": text = BIO
    elif call.data == "politics": text = POLITICS
    elif call.data == "public": text = PUBLIC
    elif call.data == "awards": text = AWARDS
    elif call.data == "svo": text = SVO
    elif call.data == "contacts": text = CONTACTS

    if text:
        back = types.InlineKeyboardMarkup()
        back.add(types.InlineKeyboardButton("← Назад в меню", callback_data="back"))
        try:
            bot.edit_message_text(
                chat_id=call.message.chat.id,
                message_id=call.message.message_id,
                text=text,
                parse_mode='Markdown',
                reply_markup=back
            )
        except:
            pass

    if call.data == "back":
        bot.edit_message_text(
            chat_id=call.message.chat.id,
            message_id=call.message.message_id,
            text="Выберите нужный раздел 👇",
            reply_markup=main_menu()
        )

@app.route('/webhook', methods=['POST'])
def webhook():
    json_string = request.get_data().decode('utf-8')
    update = telebot.types.Update.de_json(json_string)
    bot.process_new_updates([update])
    return '', 200

